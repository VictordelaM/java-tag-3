const num = 5;
const anotherNum = 8;

function compare(num, anotherNum){
    console.log(num == anotherNum)
    console.log(num > anotherNum)
    console.log(num < anotherNum)
    console.log(num >= anotherNum)
    console.log(num <= anotherNum)
    console.log(num == "5")
    console.log(num != 8)
    console.log(num != "5")
}

compare(num, anotherNum)